const card1 = document.getElementById("card1");
const img1 = card1.querySelector("img");
const text1 = card1.querySelector("h3");
const originalSrc1 = img1.src;

card1.addEventListener("mouseenter", function () {
    img1.src = "https://assets.promediateknologi.id/crop/0x0:0x0/0x0/webp/photo/indizone/2020/03/30/1xsOPA/t_5e81f00c4785f.jpg";
    text1.style.color = "green";
    text1.style.transition = "color 0.2s ease-in-out";
});

card1.addEventListener("mouseleave", function () {
    img1.src = originalSrc1;
    text1.style.color = "black";
});

card1.addEventListener("click", function () {
    window.location.href = "resep.html";
});

